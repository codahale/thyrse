	XORQ R12, R8; \
	RORXQ $63, SI, R9; \
	\
	/* Result b */ \
	MOVQ (S), R10; \
	MOVQ 48(S), R11; \
	XORQ R13, R9; \
	MOVQ 96(S), R12; \
	MOVQ 144(S), R13; \
	MOVQ 192(S), R14; \
	XORQ CX, R11; \
	ROLQ $0x2c, R11; \
	XORQ DX, R12; \
	XORQ BX, R10; \
