#define KECCAK_ROUND(S, D, RC) \
	/* Prepare round */ \
	RORXQ $63, BP, BX; \
	MOVQ 16(S), R12; \
	XORQ 56(S), DX; \
	XORQ R15, BX; \
	XORQ 96(S), R12; \
	XORQ 136(S), DX; \
