	MOVQ RC, AX; \
	ORQ  R12, SI; \
	XORQ R10, AX; \
	XORQ AX, SI; \
	MOVQ SI, (D); \
	XORQ R9, R14; \
	ROLQ $0x0e, R14; \
	MOVQ R10, R15; \
	ANDQ R11, R15; \
	XORQ R14, R15; \
	MOVQ R15, 32(D); \
	XORQ R8, R13; \
	ROLQ $0x15, R13; \
	MOVQ R13, AX; \
	ANDQ R14, AX; \
	XORQ R12, AX; \
	MOVQ AX, 16(D); \
	NOTQ R12; \
	ORQ  R10, R14; \
	ORQ  R13, R12; \
	XORQ R13, R14; \
	XORQ R11, R12; \
	MOVQ R14, 24(D); \
	MOVQ R12, 8(D); \
	MOVQ R12, BP; \
	\
	/* Result g */ \
