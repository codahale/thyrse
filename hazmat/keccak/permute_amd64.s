	XORQ R10, AX; \
	MOVQ AX, 40(D); \
	XORQ AX, SI; \
	XORQ CX, R13; \
	ROLQ $0x2d, R13; \
	MOVQ R12, AX; \
	ANDQ R13, AX; \
	XORQ R11, AX; \
	MOVQ AX, 48(D); \
	XORQ AX, BP; \
	XORQ DX, R14; \
	ROLQ $0x3d, R14; \
	MOVQ R14, AX; \
	ORQ  R10, AX; \
	XORQ R13, AX; \
	MOVQ AX, 64(D); \
	ANDQ R11, R10; \
	XORQ R14, R10; \
	MOVQ R10, 72(D); \
	NOTQ R14; \
	XORQ R10, R15; \
	ORQ  R14, R13; \
	XORQ R12, R13; \
	MOVQ R13, 56(D); \
	\
	/* Result k */ \
	MOVQ 8(S), R10; \
	MOVQ 56(S), R11; \
	MOVQ 104(S), R12; \
	MOVQ 152(S), R13; \
	MOVQ 160(S), R14; \
