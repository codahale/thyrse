	XORQ R13, R14; \
	MOVQ R14, 144(D); \
	XORQ R11, R15; \
	\
	/* Result s */ \
	MOVQ 16(S), R10; \
	MOVQ 64(S), R11; \
	MOVQ 112(S), R12; \
	XORQ DX, R10; \
	MOVQ 120(S), R13; \
	ROLQ $0x3e, R10; \
