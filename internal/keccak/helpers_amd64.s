// ABSORB_LANE_X8_GATHER gathers one uint64 from 8 instances at the given byte
// offset from BX (data base pointer) using Z28 as the index vector
// ({0, stride, 2*stride, ..., 7*stride}), and XORs the result into Zlane.
// K1 is reset to all-ones before each gather.
#define ABSORB_LANE_X8_GATHER(offset, Zlane) \
	KXNORB	K1, K1, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Zlane


// func fastLoopAbsorb168x1(s *State1, in *byte, n int)
//
// Fused absorb-permute loop for scalar Keccak.
// Keeps state in DI between iterations.
//
// Frame: 216 bytes local (0-199 permutation scratch, 200 saved in ptr, 208 count), 24 bytes args.
// Register allocation matches p1600: DI = state, SP = scratch.
TEXT ·fastLoopAbsorb168x1(SB), $216-24
	MOVQ	s+0(FP), DI
	MOVQ	in+8(FP), AX
	MOVQ	n+16(FP), CX
	MOVQ	AX, 200(SP)
	MOVQ	CX, 208(SP)

	// Convert to internal representation (complement 6 lanes).
	NOTQ	8(DI)
	NOTQ	16(DI)
	NOTQ	64(DI)
	NOTQ	96(DI)
	NOTQ	136(DI)
	NOTQ	160(DI)

loop_x1:
	CMPQ	208(SP), $168
	JB	done_x1

	// Absorb: XOR 21 lanes from input into state.
	MOVQ	200(SP), AX
	MOVQ	0*8(AX), R10;  XORQ	R10, 0*8(DI)
	MOVQ	1*8(AX), R10;  XORQ	R10, 1*8(DI)
	MOVQ	2*8(AX), R10;  XORQ	R10, 2*8(DI)
	MOVQ	3*8(AX), R10;  XORQ	R10, 3*8(DI)
	MOVQ	4*8(AX), R10;  XORQ	R10, 4*8(DI)
	MOVQ	5*8(AX), R10;  XORQ	R10, 5*8(DI)
	MOVQ	6*8(AX), R10;  XORQ	R10, 6*8(DI)
	MOVQ	7*8(AX), R10;  XORQ	R10, 7*8(DI)
	MOVQ	8*8(AX), R10;  XORQ	R10, 8*8(DI)
	MOVQ	9*8(AX), R10;  XORQ	R10, 9*8(DI)
	MOVQ	10*8(AX), R10; XORQ	R10, 10*8(DI)
	MOVQ	11*8(AX), R10; XORQ	R10, 11*8(DI)
	MOVQ	12*8(AX), R10; XORQ	R10, 12*8(DI)
	MOVQ	13*8(AX), R10; XORQ	R10, 13*8(DI)
	MOVQ	14*8(AX), R10; XORQ	R10, 14*8(DI)
	MOVQ	15*8(AX), R10; XORQ	R10, 15*8(DI)
	MOVQ	16*8(AX), R10; XORQ	R10, 16*8(DI)
	MOVQ	17*8(AX), R10; XORQ	R10, 17*8(DI)
	MOVQ	18*8(AX), R10; XORQ	R10, 18*8(DI)
	MOVQ	19*8(AX), R10; XORQ	R10, 19*8(DI)
	MOVQ	20*8(AX), R10; XORQ	R10, 20*8(DI)
	ADDQ	$168, AX
	MOVQ	AX, 200(SP)
	SUBQ	$168, 208(SP)

	// Permute: compute column parities, then 12 unrolled rounds.
	MOVQ	(DI), SI
	MOVQ	8(DI), BP
	MOVQ	32(DI), R15
	XORQ	40(DI), SI
	XORQ	48(DI), BP
	XORQ	72(DI), R15
	XORQ	80(DI), SI
	XORQ	88(DI), BP
	XORQ	112(DI), R15
	XORQ	120(DI), SI
	XORQ	128(DI), BP
	XORQ	152(DI), R15
	XORQ	160(DI), SI
	XORQ	168(DI), BP
	MOVQ	176(DI), DX
	MOVQ	184(DI), R8
	XORQ	192(DI), R15

	KECCAK_ROUND(DI, SP, $0x000000008000808b)
	KECCAK_ROUND(SP, DI, $0x800000000000008b)
	KECCAK_ROUND(DI, SP, $0x8000000000008089)
	KECCAK_ROUND(SP, DI, $0x8000000000008003)
	KECCAK_ROUND(DI, SP, $0x8000000000008002)
	KECCAK_ROUND(SP, DI, $0x8000000000000080)
	KECCAK_ROUND(DI, SP, $0x000000000000800a)
	KECCAK_ROUND(SP, DI, $0x800000008000000a)
	KECCAK_ROUND(DI, SP, $0x8000000080008081)
	KECCAK_ROUND(SP, DI, $0x8000000000008080)
	KECCAK_ROUND(DI, SP, $0x0000000080000001)
	KECCAK_ROUND(SP, DI, $0x8000000080008008)

	JMP	loop_x1

done_x1:
	// Revert internal state.
	NOTQ	8(DI)
	NOTQ	16(DI)
	NOTQ	64(DI)
	NOTQ	96(DI)
	NOTQ	136(DI)
	NOTQ	160(DI)
	RET


// func fastLoopAbsorb168x2(s *State2, in *byte, stride, n int)
//
// Fused absorb-permute loop for SSE2 x2 Keccak.
// State2 is 25 lanes × 16 bytes (lane-major packed pairs).
//
// Frame: 824 bytes local (0-799 = 2×400 perm buffers A/B, 800-823 = saved ptrs/count), 32 bytes args.
TEXT ·fastLoopAbsorb168x2(SB), $824-32
	MOVQ	s+0(FP), DI
	MOVQ	in+8(FP), AX
	MOVQ	stride+16(FP), CX
	MOVQ	n+24(FP), DX

	// Compute in1 = in + stride; save both pointers and count.
	LEAQ	(AX)(CX*1), CX
	MOVQ	AX, 800(SP)	// in0
	MOVQ	CX, 808(SP)	// in1
	MOVQ	DX, 816(SP)	// count

	// Load 25×16 from State2 into buffer A on stack.
	VMOVDQU	0*16(DI), X0;  VMOVDQU	X0, 0*16(SP)
	VMOVDQU	1*16(DI), X0;  VMOVDQU	X0, 1*16(SP)
	VMOVDQU	2*16(DI), X0;  VMOVDQU	X0, 2*16(SP)
	VMOVDQU	3*16(DI), X0;  VMOVDQU	X0, 3*16(SP)
	VMOVDQU	4*16(DI), X0;  VMOVDQU	X0, 4*16(SP)
	VMOVDQU	5*16(DI), X0;  VMOVDQU	X0, 5*16(SP)
	VMOVDQU	6*16(DI), X0;  VMOVDQU	X0, 6*16(SP)
	VMOVDQU	7*16(DI), X0;  VMOVDQU	X0, 7*16(SP)
	VMOVDQU	8*16(DI), X0;  VMOVDQU	X0, 8*16(SP)
	VMOVDQU	9*16(DI), X0;  VMOVDQU	X0, 9*16(SP)
	VMOVDQU	10*16(DI), X0; VMOVDQU	X0, 10*16(SP)
	VMOVDQU	11*16(DI), X0; VMOVDQU	X0, 11*16(SP)
	VMOVDQU	12*16(DI), X0; VMOVDQU	X0, 12*16(SP)
	VMOVDQU	13*16(DI), X0; VMOVDQU	X0, 13*16(SP)
	VMOVDQU	14*16(DI), X0; VMOVDQU	X0, 14*16(SP)
	VMOVDQU	15*16(DI), X0; VMOVDQU	X0, 15*16(SP)
	VMOVDQU	16*16(DI), X0; VMOVDQU	X0, 16*16(SP)
	VMOVDQU	17*16(DI), X0; VMOVDQU	X0, 17*16(SP)
	VMOVDQU	18*16(DI), X0; VMOVDQU	X0, 18*16(SP)
	VMOVDQU	19*16(DI), X0; VMOVDQU	X0, 19*16(SP)
	VMOVDQU	20*16(DI), X0; VMOVDQU	X0, 20*16(SP)
	VMOVDQU	21*16(DI), X0; VMOVDQU	X0, 21*16(SP)
	VMOVDQU	22*16(DI), X0; VMOVDQU	X0, 22*16(SP)
	VMOVDQU	23*16(DI), X0; VMOVDQU	X0, 23*16(SP)
	VMOVDQU	24*16(DI), X0; VMOVDQU	X0, 24*16(SP)

loop_x2:
	CMPQ	816(SP), $168
	JB	done_x2

	// Absorb: XOR 21 lanes from two input streams.
	LEAQ	0(SP), R8
	MOVQ	800(SP), AX	// in0
	MOVQ	808(SP), CX	// in1

	// For each of 21 lanes: load from in0 and in1, XOR into packed pair at R8.
	MOVQ	0*8(AX), R10; XORQ	R10, 0*16+0(R8)
	MOVQ	0*8(CX), R10; XORQ	R10, 0*16+8(R8)
	MOVQ	1*8(AX), R10; XORQ	R10, 1*16+0(R8)
	MOVQ	1*8(CX), R10; XORQ	R10, 1*16+8(R8)
	MOVQ	2*8(AX), R10; XORQ	R10, 2*16+0(R8)
	MOVQ	2*8(CX), R10; XORQ	R10, 2*16+8(R8)
	MOVQ	3*8(AX), R10; XORQ	R10, 3*16+0(R8)
	MOVQ	3*8(CX), R10; XORQ	R10, 3*16+8(R8)
	MOVQ	4*8(AX), R10; XORQ	R10, 4*16+0(R8)
	MOVQ	4*8(CX), R10; XORQ	R10, 4*16+8(R8)
	MOVQ	5*8(AX), R10; XORQ	R10, 5*16+0(R8)
	MOVQ	5*8(CX), R10; XORQ	R10, 5*16+8(R8)
	MOVQ	6*8(AX), R10; XORQ	R10, 6*16+0(R8)
	MOVQ	6*8(CX), R10; XORQ	R10, 6*16+8(R8)
	MOVQ	7*8(AX), R10; XORQ	R10, 7*16+0(R8)
	MOVQ	7*8(CX), R10; XORQ	R10, 7*16+8(R8)
	MOVQ	8*8(AX), R10; XORQ	R10, 8*16+0(R8)
	MOVQ	8*8(CX), R10; XORQ	R10, 8*16+8(R8)
	MOVQ	9*8(AX), R10; XORQ	R10, 9*16+0(R8)
	MOVQ	9*8(CX), R10; XORQ	R10, 9*16+8(R8)
	MOVQ	10*8(AX), R10; XORQ	R10, 10*16+0(R8)
	MOVQ	10*8(CX), R10; XORQ	R10, 10*16+8(R8)
	MOVQ	11*8(AX), R10; XORQ	R10, 11*16+0(R8)
	MOVQ	11*8(CX), R10; XORQ	R10, 11*16+8(R8)
	MOVQ	12*8(AX), R10; XORQ	R10, 12*16+0(R8)
	MOVQ	12*8(CX), R10; XORQ	R10, 12*16+8(R8)
	MOVQ	13*8(AX), R10; XORQ	R10, 13*16+0(R8)
	MOVQ	13*8(CX), R10; XORQ	R10, 13*16+8(R8)
	MOVQ	14*8(AX), R10; XORQ	R10, 14*16+0(R8)
	MOVQ	14*8(CX), R10; XORQ	R10, 14*16+8(R8)
	MOVQ	15*8(AX), R10; XORQ	R10, 15*16+0(R8)
	MOVQ	15*8(CX), R10; XORQ	R10, 15*16+8(R8)
	MOVQ	16*8(AX), R10; XORQ	R10, 16*16+0(R8)
	MOVQ	16*8(CX), R10; XORQ	R10, 16*16+8(R8)
	MOVQ	17*8(AX), R10; XORQ	R10, 17*16+0(R8)
	MOVQ	17*8(CX), R10; XORQ	R10, 17*16+8(R8)
	MOVQ	18*8(AX), R10; XORQ	R10, 18*16+0(R8)
	MOVQ	18*8(CX), R10; XORQ	R10, 18*16+8(R8)
	MOVQ	19*8(AX), R10; XORQ	R10, 19*16+0(R8)
	MOVQ	19*8(CX), R10; XORQ	R10, 19*16+8(R8)
	MOVQ	20*8(AX), R10; XORQ	R10, 20*16+0(R8)
	MOVQ	20*8(CX), R10; XORQ	R10, 20*16+8(R8)

	ADDQ	$168, AX
	ADDQ	$168, CX
	MOVQ	AX, 800(SP)
	MOVQ	CX, 808(SP)
	SUBQ	$168, 816(SP)

	// Permute: 12-round inner loop (identical to p1600x2Lane).
	LEAQ	0(SP), R8
	LEAQ	400(SP), R9
	LEAQ	round_consts_2x+192(SB), R11
	MOVQ	$12, R10

	PCALIGN	$16
round_loop_x2:
	X2_KECCAK_ROUND
	XCHGQ	R8, R9
	ADDQ	$16, R11
	SUBQ	$1, R10
	JNZ	round_loop_x2

	JMP	loop_x2

done_x2:
	// Store 25×16 from buffer A to State2.
	// After 12 rounds (even), state is in buffer A at 0(SP).
	LEAQ	0(SP), R8
	VMOVDQU	0*16(R8), X0;  VMOVDQU	X0, 0*16(DI)
	VMOVDQU	1*16(R8), X0;  VMOVDQU	X0, 1*16(DI)
	VMOVDQU	2*16(R8), X0;  VMOVDQU	X0, 2*16(DI)
	VMOVDQU	3*16(R8), X0;  VMOVDQU	X0, 3*16(DI)
	VMOVDQU	4*16(R8), X0;  VMOVDQU	X0, 4*16(DI)
	VMOVDQU	5*16(R8), X0;  VMOVDQU	X0, 5*16(DI)
	VMOVDQU	6*16(R8), X0;  VMOVDQU	X0, 6*16(DI)
	VMOVDQU	7*16(R8), X0;  VMOVDQU	X0, 7*16(DI)
	VMOVDQU	8*16(R8), X0;  VMOVDQU	X0, 8*16(DI)
	VMOVDQU	9*16(R8), X0;  VMOVDQU	X0, 9*16(DI)
	VMOVDQU	10*16(R8), X0; VMOVDQU	X0, 10*16(DI)
	VMOVDQU	11*16(R8), X0; VMOVDQU	X0, 11*16(DI)
	VMOVDQU	12*16(R8), X0; VMOVDQU	X0, 12*16(DI)
	VMOVDQU	13*16(R8), X0; VMOVDQU	X0, 13*16(DI)
	VMOVDQU	14*16(R8), X0; VMOVDQU	X0, 14*16(DI)
	VMOVDQU	15*16(R8), X0; VMOVDQU	X0, 15*16(DI)
	VMOVDQU	16*16(R8), X0; VMOVDQU	X0, 16*16(DI)
	VMOVDQU	17*16(R8), X0; VMOVDQU	X0, 17*16(DI)
	VMOVDQU	18*16(R8), X0; VMOVDQU	X0, 18*16(DI)
	VMOVDQU	19*16(R8), X0; VMOVDQU	X0, 19*16(DI)
	VMOVDQU	20*16(R8), X0; VMOVDQU	X0, 20*16(DI)
	VMOVDQU	21*16(R8), X0; VMOVDQU	X0, 21*16(DI)
	VMOVDQU	22*16(R8), X0; VMOVDQU	X0, 22*16(DI)
	VMOVDQU	23*16(R8), X0; VMOVDQU	X0, 23*16(DI)
	VMOVDQU	24*16(R8), X0; VMOVDQU	X0, 24*16(DI)
	RET


// func fastLoopAbsorb168x4(s *State4, in *byte, stride, n int)
//
// Fused absorb-permute loop for AVX2 x4 Keccak.
// State4 is 25 lanes × 32 bytes (lane-major packed quads).
//
// Frame: 1640 bytes local (0-1599 = 2×800 perm buffers A/B, 1600-1639 = 4 input ptrs + count), 32 bytes args.
TEXT ·fastLoopAbsorb168x4(SB), $1640-32
	MOVQ	s+0(FP), DI
	MOVQ	in+8(FP), AX
	MOVQ	stride+16(FP), CX
	MOVQ	n+24(FP), DX

	// Compute 4 input pointers: in + {0, 1, 2, 3}×stride.
	MOVQ	AX, 1600(SP)		// in0
	LEAQ	(AX)(CX*1), R10
	MOVQ	R10, 1608(SP)		// in1
	LEAQ	(R10)(CX*1), R10
	MOVQ	R10, 1616(SP)		// in2
	LEAQ	(R10)(CX*1), R10
	MOVQ	R10, 1624(SP)		// in3
	MOVQ	DX, 1632(SP)		// count

	// Load 25×32 from State4 into buffer A on stack.
	VMOVDQU	0*32(DI), Y0;  VMOVDQU	Y0, 0*32(SP)
	VMOVDQU	1*32(DI), Y0;  VMOVDQU	Y0, 1*32(SP)
	VMOVDQU	2*32(DI), Y0;  VMOVDQU	Y0, 2*32(SP)
	VMOVDQU	3*32(DI), Y0;  VMOVDQU	Y0, 3*32(SP)
	VMOVDQU	4*32(DI), Y0;  VMOVDQU	Y0, 4*32(SP)
	VMOVDQU	5*32(DI), Y0;  VMOVDQU	Y0, 5*32(SP)
	VMOVDQU	6*32(DI), Y0;  VMOVDQU	Y0, 6*32(SP)
	VMOVDQU	7*32(DI), Y0;  VMOVDQU	Y0, 7*32(SP)
