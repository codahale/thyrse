	VMOVDQU	23*32(DI), Y0; VMOVDQU	Y0, 23*32(SP)
	VMOVDQU	24*32(DI), Y0; VMOVDQU	Y0, 24*32(SP)

loop_x4:
	CMPQ	1632(SP), $168
	JB	done_x4

	// Absorb: XOR 21 lanes from 4 input streams into packed quads at R8.
	LEAQ	0(SP), R8
	MOVQ	1600(SP), AX	// in0
	MOVQ	1608(SP), CX	// in1
	MOVQ	1616(SP), DX	// in2
	MOVQ	1624(SP), BX	// in3

