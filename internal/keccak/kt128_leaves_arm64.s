	VST1	[V0.D1], (R6); ADD $8, R6
	VST1	[V1.D1], (R6); ADD $8, R6
	VST1	[V2.D1], (R6); ADD $8, R6
	VST1	[V3.D1], (R6); ADD $8, R6
	// Instance 1: upper 64 bits of V0-V3.
