	MOVQ	SI, 1632(SP)
	MOVQ	BP, 1640(SP)
	MOVQ	R13, 1648(SP)
	MOVQ	R14, 1656(SP)
	SUBQ	$1, 1664(SP)

	LEAQ	0(SP), R8
	LEAQ	800(SP), R9
	LEAQ	round_consts_4x+384(SB), R11
	MOVQ	$12, R10

	PCALIGN	$16
tw128_enc_avx2_round_a:
	X4_KECCAK_ROUND
	XCHGQ	R8, R9
	ADDQ	$32, R11
	SUBQ	$1, R10
	JNZ	tw128_enc_avx2_round_a

	JMP	tw128_enc_avx2_loop_a

