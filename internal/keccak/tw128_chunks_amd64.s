	ENCRYPT_LANE_X4(17)
	ENCRYPT_LANE_X4(18)
	ENCRYPT_LANE_X4(19)
	ENCRYPT_LANE_X4(20)

	ADDQ	$168, AX
	ADDQ	$168, CX
	ADDQ	$168, DX
	ADDQ	$168, BX
	ADDQ	$168, SI
	ADDQ	$168, BP
	ADDQ	$168, R13
