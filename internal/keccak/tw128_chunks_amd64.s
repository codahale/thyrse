	VMOVDQU64	0*64(AX), Z0
	VMOVDQU64	1*64(AX), Z1
	VMOVDQU64	2*64(AX), Z2
	VMOVDQU64	3*64(AX), Z3
	VMOVDQU64	4*64(AX), Z4
	VMOVDQU64	5*64(AX), Z5
	VMOVDQU64	6*64(AX), Z6
	VMOVDQU64	7*64(AX), Z7
	VMOVDQU64	8*64(AX), Z8
	VMOVDQU64	9*64(AX), Z9
	VMOVDQU64	10*64(AX), Z10
	VMOVDQU64	11*64(AX), Z11
	VMOVDQU64	12*64(AX), Z12
	VMOVDQU64	13*64(AX), Z13
	VMOVDQU64	14*64(AX), Z14
	VMOVDQU64	15*64(AX), Z15
	VMOVDQU64	16*64(AX), Z16
	VMOVDQU64	17*64(AX), Z17
	VMOVDQU64	18*64(AX), Z18
	VMOVDQU64	19*64(AX), Z19
	VMOVDQU64	20*64(AX), Z20
	VMOVDQU64	21*64(AX), Z21
	VMOVDQU64	22*64(AX), Z22
	VMOVDQU64	23*64(AX), Z23
	VMOVDQU64	24*64(AX), Z24

	MOVQ	$48, R12

tw128_dec_avx512_loop:
	VMOVDQU64	0(SP), Z28

	DECRYPT_LANE_X8_GS(0*8, Z0)
	DECRYPT_LANE_X8_GS(1*8, Z1)
	DECRYPT_LANE_X8_GS(2*8, Z2)
	DECRYPT_LANE_X8_GS(3*8, Z3)
	DECRYPT_LANE_X8_GS(4*8, Z4)
	DECRYPT_LANE_X8_GS(5*8, Z5)
	DECRYPT_LANE_X8_GS(6*8, Z6)
	DECRYPT_LANE_X8_GS(7*8, Z7)
	DECRYPT_LANE_X8_GS(8*8, Z8)
	DECRYPT_LANE_X8_GS(9*8, Z9)
	DECRYPT_LANE_X8_GS(10*8, Z10)
	DECRYPT_LANE_X8_GS(11*8, Z11)
	DECRYPT_LANE_X8_GS(12*8, Z12)
	DECRYPT_LANE_X8_GS(13*8, Z13)
	DECRYPT_LANE_X8_GS(14*8, Z14)
	DECRYPT_LANE_X8_GS(15*8, Z15)
	DECRYPT_LANE_X8_GS(16*8, Z16)
	DECRYPT_LANE_X8_GS(17*8, Z17)
	DECRYPT_LANE_X8_GS(18*8, Z18)
	DECRYPT_LANE_X8_GS(19*8, Z19)
	DECRYPT_LANE_X8_GS(20*8, Z20)

	LEAQ	round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	ADDQ	$168, BX
	ADDQ	$168, R14
	SUBQ	$1, R12
	JNZ	tw128_dec_avx512_loop

	// Decrypt final 16 lanes.
	VMOVDQU64	0(SP), Z28
	DECRYPT_LANE_X8_GS(0*8, Z0)
	DECRYPT_LANE_X8_GS(1*8, Z1)
	DECRYPT_LANE_X8_GS(2*8, Z2)
	DECRYPT_LANE_X8_GS(3*8, Z3)
	DECRYPT_LANE_X8_GS(4*8, Z4)
	DECRYPT_LANE_X8_GS(5*8, Z5)
	DECRYPT_LANE_X8_GS(6*8, Z6)
	DECRYPT_LANE_X8_GS(7*8, Z7)
	DECRYPT_LANE_X8_GS(8*8, Z8)
	DECRYPT_LANE_X8_GS(9*8, Z9)
	DECRYPT_LANE_X8_GS(10*8, Z10)
	DECRYPT_LANE_X8_GS(11*8, Z11)
	DECRYPT_LANE_X8_GS(12*8, Z12)
	DECRYPT_LANE_X8_GS(13*8, Z13)
	DECRYPT_LANE_X8_GS(14*8, Z14)
	DECRYPT_LANE_X8_GS(15*8, Z15)

	VPBROADCASTQ_IMM_0x0B(Z25)
	VPXORQ	Z25, Z16, Z16
	VPBROADCASTQ_IMM_0x80_HIGH(Z25)
	VPXORQ	Z25, Z20, Z20

	LEAQ	round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	// Extract CVs via VPSCATTERQQ.
	MOVQ	$0, 0(SP)
	MOVQ	$32, 8(SP)
	MOVQ	$64, 16(SP)
	MOVQ	$96, 24(SP)
	MOVQ	$128, 32(SP)
	MOVQ	$160, 40(SP)
	MOVQ	$192, 48(SP)
	MOVQ	$224, 56(SP)
	VMOVDQU64	0(SP), Z28

	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z0, K1, 0(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z1, K1, 8(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z2, K1, 16(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z3, K1, 24(DI)(Z28*1)

	VZEROUPPER
	RET


// func encryptChunksTW128AVX2(s *State8, src, dst *byte, cvs *byte)
//
// 2× x4 approach (half 0: instances 0-3, half 1: instances 4-7).
//
// Frame: 1688 bytes local (0-799 = buffer A, 800-1599 = buffer B,
//   1600-1631 = 4 src ptrs, 1632-1663 = 4 dst ptrs,
//   1664 = count, 1672 = saved state ptr, 1680 = saved cvs ptr), 32 bytes args.
TEXT ·encryptChunksTW128AVX2(SB), $1688-32
	MOVQ	s+0(FP), DI
	MOVQ	src+8(FP), AX
	MOVQ	dst+16(FP), R9
	MOVQ	cvs+24(FP), R15
	MOVQ	DI, 1672(SP)		// save state ptr
	MOVQ	R15, 1680(SP)		// save cvs ptr

	// === Half 0: instances 0-3 ===
	// Src pointers: src + {0, 1, 2, 3}×8192.
	MOVQ	AX, 1600(SP)
	LEAQ	8192(AX), R10
	MOVQ	R10, 1608(SP)
	LEAQ	8192(R10), R10
	MOVQ	R10, 1616(SP)
	LEAQ	8192(R10), R10
	MOVQ	R10, 1624(SP)
	// Dst pointers: dst + {0, 1, 2, 3}×8192.
	MOVQ	R9, 1632(SP)
	LEAQ	8192(R9), R10
	MOVQ	R10, 1640(SP)
	LEAQ	8192(R10), R10
	MOVQ	R10, 1648(SP)
	LEAQ	8192(R10), R10
	MOVQ	R10, 1656(SP)
	MOVQ	$48, 1664(SP)		// stripe count

	// Load instances 0-3 from State8.
	VMOVDQU	0*64(DI), Y0;   VMOVDQU	Y0, 0*32(SP)
	VMOVDQU	1*64(DI), Y0;   VMOVDQU	Y0, 1*32(SP)
	VMOVDQU	2*64(DI), Y0;   VMOVDQU	Y0, 2*32(SP)
	VMOVDQU	3*64(DI), Y0;   VMOVDQU	Y0, 3*32(SP)
	VMOVDQU	4*64(DI), Y0;   VMOVDQU	Y0, 4*32(SP)
	VMOVDQU	5*64(DI), Y0;   VMOVDQU	Y0, 5*32(SP)
	VMOVDQU	6*64(DI), Y0;   VMOVDQU	Y0, 6*32(SP)
	VMOVDQU	7*64(DI), Y0;   VMOVDQU	Y0, 7*32(SP)
	VMOVDQU	8*64(DI), Y0;   VMOVDQU	Y0, 8*32(SP)
	VMOVDQU	9*64(DI), Y0;   VMOVDQU	Y0, 9*32(SP)
	VMOVDQU	10*64(DI), Y0;  VMOVDQU	Y0, 10*32(SP)
	VMOVDQU	11*64(DI), Y0;  VMOVDQU	Y0, 11*32(SP)
	VMOVDQU	12*64(DI), Y0;  VMOVDQU	Y0, 12*32(SP)
	VMOVDQU	13*64(DI), Y0;  VMOVDQU	Y0, 13*32(SP)
	VMOVDQU	14*64(DI), Y0;  VMOVDQU	Y0, 14*32(SP)
	VMOVDQU	15*64(DI), Y0;  VMOVDQU	Y0, 15*32(SP)
	VMOVDQU	16*64(DI), Y0;  VMOVDQU	Y0, 16*32(SP)
	VMOVDQU	17*64(DI), Y0;  VMOVDQU	Y0, 17*32(SP)
	VMOVDQU	18*64(DI), Y0;  VMOVDQU	Y0, 18*32(SP)
	VMOVDQU	19*64(DI), Y0;  VMOVDQU	Y0, 19*32(SP)
	VMOVDQU	20*64(DI), Y0;  VMOVDQU	Y0, 20*32(SP)
	VMOVDQU	21*64(DI), Y0;  VMOVDQU	Y0, 21*32(SP)
	VMOVDQU	22*64(DI), Y0;  VMOVDQU	Y0, 22*32(SP)
	VMOVDQU	23*64(DI), Y0;  VMOVDQU	Y0, 23*32(SP)
	VMOVDQU	24*64(DI), Y0;  VMOVDQU	Y0, 24*32(SP)

tw128_enc_avx2_loop_a:
	CMPQ	1664(SP), $0
	JEQ	tw128_enc_avx2_final_a

	LEAQ	0(SP), R8
	MOVQ	1600(SP), AX
	MOVQ	1608(SP), CX
	MOVQ	1616(SP), DX
	MOVQ	1624(SP), BX
	MOVQ	1632(SP), SI
	MOVQ	1640(SP), BP
	MOVQ	1648(SP), R13
	MOVQ	1656(SP), R14

	ENCRYPT_LANE_X4(0)
	ENCRYPT_LANE_X4(1)
	ENCRYPT_LANE_X4(2)
	ENCRYPT_LANE_X4(3)
	ENCRYPT_LANE_X4(4)
	ENCRYPT_LANE_X4(5)
	ENCRYPT_LANE_X4(6)
	ENCRYPT_LANE_X4(7)
	ENCRYPT_LANE_X4(8)
	ENCRYPT_LANE_X4(9)
	ENCRYPT_LANE_X4(10)
	ENCRYPT_LANE_X4(11)
	ENCRYPT_LANE_X4(12)
	ENCRYPT_LANE_X4(13)
	ENCRYPT_LANE_X4(14)
