// Fused TW128 chunk processing — AVX-512 and AVX2 implementations.
//
// Each function processes 8 × 8192-byte chunks in a single call,
// encrypting/decrypting and writing 8 × 32-byte CVs to a byte buffer.

//go:build !purego

#include "textflag.h"
#include "permute_amd64_avx2.h"
#include "permute_amd64_avx512.h"

// ENCRYPT_LANE_X8_GS encrypts one full lane using gather (from BX+Z28) and scatter (to R14+Z28).
// XORs gathered data into Zlane, then scatters the result.
#define ENCRYPT_LANE_X8_GS(offset, Zlane) \
	KXNORB	K1, K1, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Zlane; \
	KXNORB	K1, K1, K1; \
	VPSCATTERQQ	Zlane, K1, offset(R14)(Z28*1)

// DECRYPT_LANE_X8_GS decrypts one full lane: pt = ct ^ state, state = ct.
#define DECRYPT_LANE_X8_GS(offset, Zlane) \
	KXNORB	K1, K1, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Z26; \
	KXNORB	K1, K1, K1; \
	VPSCATTERQQ	Z26, K1, offset(R14)(Z28*1); \
	VMOVDQA64	Z25, Zlane

// VPBROADCASTQ_IMM_0x0B broadcasts 0x0B to all 8 qwords in Zdst.
#define VPBROADCASTQ_IMM_0x0B(Zdst) \
	MOVQ	$0x0B, AX; \
	VPBROADCASTQ	AX, Zdst

// VPBROADCASTQ_IMM_0x80_HIGH broadcasts 0x8000000000000000 to all 8 qwords in Zdst.
#define VPBROADCASTQ_IMM_0x80_HIGH(Zdst) \
	MOVQ	$0x8000000000000000, AX; \
	VPBROADCASTQ	AX, Zdst

// ENCRYPT_LANE_X4 encrypts lane i for 4 instances.
// AX=src0, CX=src1, DX=src2, BX=src3, SI=dst0, BP=dst1, R13=dst2, R14=dst3,
// R8=state buffer. Clobbers R10.
#define ENCRYPT_LANE_X4(i) \
	MOVQ i*8(AX), R10; XORQ R10, i*32+0(R8);  MOVQ i*32+0(R8), R10;  MOVQ R10, i*8(SI);  \
	MOVQ i*8(CX), R10; XORQ R10, i*32+8(R8);  MOVQ i*32+8(R8), R10;  MOVQ R10, i*8(BP);  \
	MOVQ i*8(DX), R10; XORQ R10, i*32+16(R8); MOVQ i*32+16(R8), R10; MOVQ R10, i*8(R13); \
	MOVQ i*8(BX), R10; XORQ R10, i*32+24(R8); MOVQ i*32+24(R8), R10; MOVQ R10, i*8(R14)

// DECRYPT_LANE_X4 decrypts lane i for 4 instances.
// Same register allocation. Clobbers R10, R11.
#define DECRYPT_LANE_X4(i) \
	MOVQ i*8(AX), R10; MOVQ i*32+0(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(SI);  MOVQ R10, i*32+0(R8);  \
	MOVQ i*8(CX), R10; MOVQ i*32+8(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(BP);  MOVQ R10, i*32+8(R8);  \
	MOVQ i*8(DX), R10; MOVQ i*32+16(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R13); MOVQ R10, i*32+16(R8); \
	MOVQ i*8(BX), R10; MOVQ i*32+24(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R14); MOVQ R10, i*32+24(R8)


// func encryptChunksTW128AVX512(s *State8, src, dst *byte, cvs *byte)
//
// Processes 8 × 8192-byte chunks, encrypting and writing 8 × 32-byte CVs.
//
// TW128 chunk constants (hardcoded):
//   Rate = 168, DS = 0x0B, BlockSize = 8192
//   48 full 168-byte stripes per 8192-byte chunk
//   128-byte remainder = 16 lanes
//
// Frame: 64 bytes local (gather indices), 32 bytes args.
TEXT ·encryptChunksTW128AVX512(SB), $64-32
	MOVQ	s+0(FP), AX
	MOVQ	src+8(FP), BX
	MOVQ	dst+16(FP), R14
	MOVQ	cvs+24(FP), DI

	// Build gather/scatter index vector {0, 8192, 2×8192, ..., 7×8192} at SP+0.
	MOVQ	$0, 0(SP)
	MOVQ	$8192, 8(SP)
	MOVQ	$16384, 16(SP)
	MOVQ	$24576, 24(SP)
	MOVQ	$32768, 32(SP)
	MOVQ	$40960, 40(SP)
	MOVQ	$49152, 48(SP)
	MOVQ	$57344, 56(SP)

	// Load state from State8 into Z0-Z24.
	VMOVDQU64	0*64(AX), Z0
	VMOVDQU64	1*64(AX), Z1
	VMOVDQU64	2*64(AX), Z2
	VMOVDQU64	3*64(AX), Z3
	VMOVDQU64	4*64(AX), Z4
	VMOVDQU64	5*64(AX), Z5
	VMOVDQU64	6*64(AX), Z6
	VMOVDQU64	7*64(AX), Z7
	VMOVDQU64	8*64(AX), Z8
	VMOVDQU64	9*64(AX), Z9
	VMOVDQU64	10*64(AX), Z10
	VMOVDQU64	11*64(AX), Z11
	VMOVDQU64	12*64(AX), Z12
	VMOVDQU64	13*64(AX), Z13
	VMOVDQU64	14*64(AX), Z14
	VMOVDQU64	15*64(AX), Z15
	VMOVDQU64	16*64(AX), Z16
	VMOVDQU64	17*64(AX), Z17
	VMOVDQU64	18*64(AX), Z18
	VMOVDQU64	19*64(AX), Z19
	VMOVDQU64	20*64(AX), Z20
	VMOVDQU64	21*64(AX), Z21
	VMOVDQU64	22*64(AX), Z22
	VMOVDQU64	23*64(AX), Z23
	VMOVDQU64	24*64(AX), Z24

	// Loop 48 full stripes.
	MOVQ	$48, R12

tw128_enc_avx512_loop:
	VMOVDQU64	0(SP), Z28

	ENCRYPT_LANE_X8_GS(0*8, Z0)
	ENCRYPT_LANE_X8_GS(1*8, Z1)
	ENCRYPT_LANE_X8_GS(2*8, Z2)
	ENCRYPT_LANE_X8_GS(3*8, Z3)
	ENCRYPT_LANE_X8_GS(4*8, Z4)
	ENCRYPT_LANE_X8_GS(5*8, Z5)
	ENCRYPT_LANE_X8_GS(6*8, Z6)
	ENCRYPT_LANE_X8_GS(7*8, Z7)
	ENCRYPT_LANE_X8_GS(8*8, Z8)
	ENCRYPT_LANE_X8_GS(9*8, Z9)
	ENCRYPT_LANE_X8_GS(10*8, Z10)
	ENCRYPT_LANE_X8_GS(11*8, Z11)
	ENCRYPT_LANE_X8_GS(12*8, Z12)
	ENCRYPT_LANE_X8_GS(13*8, Z13)
	ENCRYPT_LANE_X8_GS(14*8, Z14)
	ENCRYPT_LANE_X8_GS(15*8, Z15)
	ENCRYPT_LANE_X8_GS(16*8, Z16)
	ENCRYPT_LANE_X8_GS(17*8, Z17)
	ENCRYPT_LANE_X8_GS(18*8, Z18)
	ENCRYPT_LANE_X8_GS(19*8, Z19)
	ENCRYPT_LANE_X8_GS(20*8, Z20)

	LEAQ	round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	ADDQ	$168, BX
	ADDQ	$168, R14
	SUBQ	$1, R12
	JNZ	tw128_enc_avx512_loop

	// Encrypt final 16 lanes (128-byte remainder).
	VMOVDQU64	0(SP), Z28
	ENCRYPT_LANE_X8_GS(0*8, Z0)
	ENCRYPT_LANE_X8_GS(1*8, Z1)
	ENCRYPT_LANE_X8_GS(2*8, Z2)
	ENCRYPT_LANE_X8_GS(3*8, Z3)
	ENCRYPT_LANE_X8_GS(4*8, Z4)
	ENCRYPT_LANE_X8_GS(5*8, Z5)
	ENCRYPT_LANE_X8_GS(6*8, Z6)
	ENCRYPT_LANE_X8_GS(7*8, Z7)
	ENCRYPT_LANE_X8_GS(8*8, Z8)
	ENCRYPT_LANE_X8_GS(9*8, Z9)
	ENCRYPT_LANE_X8_GS(10*8, Z10)
	ENCRYPT_LANE_X8_GS(11*8, Z11)
	ENCRYPT_LANE_X8_GS(12*8, Z12)
	ENCRYPT_LANE_X8_GS(13*8, Z13)
	ENCRYPT_LANE_X8_GS(14*8, Z14)
	ENCRYPT_LANE_X8_GS(15*8, Z15)

	// XOR padding: DS=0x0B into lane 16, pad10*1 end 0x80 into lane 20.
	VPBROADCASTQ_IMM_0x0B(Z25)
	VPXORQ	Z25, Z16, Z16
	VPBROADCASTQ_IMM_0x80_HIGH(Z25)
	VPXORQ	Z25, Z20, Z20

	// Final permutation.
	LEAQ	round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	// Extract CVs via VPSCATTERQQ.
	MOVQ	$0, 0(SP)
	MOVQ	$32, 8(SP)
	MOVQ	$64, 16(SP)
	MOVQ	$96, 24(SP)
	MOVQ	$128, 32(SP)
	MOVQ	$160, 40(SP)
	MOVQ	$192, 48(SP)
	MOVQ	$224, 56(SP)
	VMOVDQU64	0(SP), Z28

	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z0, K1, 0(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z1, K1, 8(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z2, K1, 16(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z3, K1, 24(DI)(Z28*1)

	VZEROUPPER
	RET


// func decryptChunksTW128AVX512(s *State8, src, dst *byte, cvs *byte)
TEXT ·decryptChunksTW128AVX512(SB), $64-32
	MOVQ	s+0(FP), AX
	MOVQ	src+8(FP), BX
	MOVQ	dst+16(FP), R14
	MOVQ	cvs+24(FP), DI

	MOVQ	$0, 0(SP)
	MOVQ	$8192, 8(SP)
	MOVQ	$16384, 16(SP)
	MOVQ	$24576, 24(SP)
	MOVQ	$32768, 32(SP)
