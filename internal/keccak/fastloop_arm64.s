	VST1.P	[V16.D1, V17.D1, V18.D1, V19.D1], 32(R0)
	VST1.P	[V20.D1, V21.D1, V22.D1, V23.D1], 32(R0)
	VST1	[V24.D1], (R0)

	RET

// func fastLoopAbsorb168x1(s *State1, in *byte, n int)
