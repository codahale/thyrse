// ENCRYPT_LANE_X4 encrypts lane i for 4 instances.
// AX=src0, CX=src1, DX=src2, BX=src3, SI=dst0, BP=dst1, R13=dst2, R14=dst3,
// R8=state buffer. Clobbers R10.
#define ENCRYPT_LANE_X4(i) \
	MOVQ i*8(AX), R10; XORQ R10, i*32+0(R8);  MOVQ i*32+0(R8), R10;  MOVQ R10, i*8(SI);  \
	MOVQ i*8(CX), R10; XORQ R10, i*32+8(R8);  MOVQ i*32+8(R8), R10;  MOVQ R10, i*8(BP);  \
	MOVQ i*8(DX), R10; XORQ R10, i*32+16(R8); MOVQ i*32+16(R8), R10; MOVQ R10, i*8(R13); \
	MOVQ i*8(BX), R10; XORQ R10, i*32+24(R8); MOVQ i*32+24(R8), R10; MOVQ R10, i*8(R14)

// DECRYPT_LANE_X4 decrypts lane i for 4 instances.
// Same register allocation. Clobbers R10, R11.
#define DECRYPT_LANE_X4(i) \
	MOVQ i*8(AX), R10; MOVQ i*32+0(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(SI);  MOVQ R10, i*32+0(R8);  \
	MOVQ i*8(CX), R10; MOVQ i*32+8(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(BP);  MOVQ R10, i*32+8(R8);  \
	MOVQ i*8(DX), R10; MOVQ i*32+16(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R13); MOVQ R10, i*32+16(R8); \
	MOVQ i*8(BX), R10; MOVQ i*32+24(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R14); MOVQ R10, i*32+24(R8)


// func fastLoopEncrypt168x8AVX2(s *State8, src, dst *byte, stride, n int)
