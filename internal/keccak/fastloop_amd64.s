// ENCRYPT_LANE_X8_GS encrypts one full lane using gather (from BX+Z28) and scatter (to R14+Z28).
// XORs gathered data into Zlane, then scatters the result.
#define ENCRYPT_LANE_X8_GS(offset, Zlane) \
	KXNORB	K1, K1, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Zlane; \
	KXNORB	K1, K1, K1; \
	VPSCATTERQQ	Zlane, K1, offset(R14)(Z28*1)

// DECRYPT_LANE_X8_GS decrypts one full lane: pt = ct ^ state, state = ct.
#define DECRYPT_LANE_X8_GS(offset, Zlane) \
	KXNORB	K1, K1, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Z26; \
	KXNORB	K1, K1, K1; \
	VPSCATTERQQ	Z26, K1, offset(R14)(Z28*1); \
	VMOVDQA64	Z25, Zlane


