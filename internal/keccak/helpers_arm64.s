// ABSORB_STRIPE_X2 XORs one 168-byte stripe from two input pointers (IN0, IN1)
// into state registers V0-V20 (21 rate lanes). Uses V25-V26 as temps.
// Each lane is {in0_val, in1_val} packed into a 128-bit vector.
