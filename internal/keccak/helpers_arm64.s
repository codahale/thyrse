	VLD1	(IN), [V25.D1]; ADD $8, IN; VEOR V25.B8, V20.B8, V20.B8

// ENCRYPT_STRIPE_X2 encrypts 20 full lanes + 7-byte lane 20 from two input
// pointers (SRC0, SRC1) into state registers V0-V20 (.D2), writing ciphertext
// to DST0, DST1. Uses V25-V27 as temps, R7=padWord. Clobbers R8, R9, R10, R11.
#define ENCRYPT_STRIPE_X2(SRC0, SRC1, DST0, DST1) \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V0.B16, V0.B16; VST1 [V0.D1], (DST0); ADD $8, DST0; VDUP V0.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V1.B16, V1.B16; VST1 [V1.D1], (DST0); ADD $8, DST0; VDUP V1.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V2.B16, V2.B16; VST1 [V2.D1], (DST0); ADD $8, DST0; VDUP V2.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V3.B16, V3.B16; VST1 [V3.D1], (DST0); ADD $8, DST0; VDUP V3.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V4.B16, V4.B16; VST1 [V4.D1], (DST0); ADD $8, DST0; VDUP V4.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V5.B16, V5.B16; VST1 [V5.D1], (DST0); ADD $8, DST0; VDUP V5.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V6.B16, V6.B16; VST1 [V6.D1], (DST0); ADD $8, DST0; VDUP V6.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V7.B16, V7.B16; VST1 [V7.D1], (DST0); ADD $8, DST0; VDUP V7.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V8.B16, V8.B16; VST1 [V8.D1], (DST0); ADD $8, DST0; VDUP V8.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V9.B16, V9.B16; VST1 [V9.D1], (DST0); ADD $8, DST0; VDUP V9.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V10.B16, V10.B16; VST1 [V10.D1], (DST0); ADD $8, DST0; VDUP V10.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V11.B16, V11.B16; VST1 [V11.D1], (DST0); ADD $8, DST0; VDUP V11.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V12.B16, V12.B16; VST1 [V12.D1], (DST0); ADD $8, DST0; VDUP V12.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V13.B16, V13.B16; VST1 [V13.D1], (DST0); ADD $8, DST0; VDUP V13.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V14.B16, V14.B16; VST1 [V14.D1], (DST0); ADD $8, DST0; VDUP V14.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V15.B16, V15.B16; VST1 [V15.D1], (DST0); ADD $8, DST0; VDUP V15.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V16.B16, V16.B16; VST1 [V16.D1], (DST0); ADD $8, DST0; VDUP V16.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V17.B16, V17.B16; VST1 [V17.D1], (DST0); ADD $8, DST0; VDUP V17.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V18.B16, V18.B16; VST1 [V18.D1], (DST0); ADD $8, DST0; VDUP V18.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	VLD1 (SRC0), [V25.D1]; ADD $8, SRC0; VLD1 (SRC1), [V26.D1]; ADD $8, SRC1; VZIP1 V26.D2, V25.D2, V25.D2; VEOR V25.B16, V19.B16, V19.B16; VST1 [V19.D1], (DST0); ADD $8, DST0; VDUP V19.D[1], V26.D2; VST1 [V26.D1], (DST1); ADD $8, DST1; \
	/* Lane 20: 7-byte encrypt for each instance. */ \
	/* Instance 0: load 7 bytes from SRC0. */ \
	MOVWU (SRC0), R8; MOVHU 4(SRC0), R9; ORR R9<<32, R8, R8; MOVBU 6(SRC0), R10; ORR R10<<48, R8, R8; \
	/* Instance 1: load 7 bytes from SRC1. */ \
	MOVWU (SRC1), R9; MOVHU 4(SRC1), R10; ORR R10<<32, R9, R9; MOVBU 6(SRC1), R11; ORR R11<<48, R9, R9; \
	/* Combine into V25 = {src1, src0} */ \
	VMOV R8, V25.D[0]; VMOV R9, V25.D[1]; \
	/* state ^= src */ \
	VEOR V25.B16, V20.B16, V20.B16; \
	/* Store 7 bytes of new state for each instance. */ \
	VMOV V20.D[0], R8; \
	MOVW R8, (DST0); LSR $32, R8, R10; MOVH R10, 4(DST0); LSR $16, R10, R10; MOVB R10, 6(DST0); \
	VMOV V20.D[1], R9; \
	MOVW R9, (DST1); LSR $32, R9, R10; MOVH R10, 4(DST1); LSR $16, R10, R10; MOVB R10, 6(DST1); \
	/* XOR padWord into both halves of lane 20. */ \
	VDUP R7, V25.D2; VEOR V25.B16, V20.B16, V20.B16

// DECRYPT_STRIPE_X2 decrypts 20 full lanes + 7-byte lane 20 from two input
