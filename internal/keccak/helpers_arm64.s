	VLD1	(IN1), [V26.D1]; ADD $8, IN1; \
	VZIP1	V26.D2, V25.D2, V25.D2; \
	VEOR	V25.B16, V1.B16, V1.B16; \
	\
	VLD1	(IN0), [V25.D1]; ADD $8, IN0; \
	VLD1	(IN1), [V26.D1]; ADD $8, IN1; \
	VZIP1	V26.D2, V25.D2, V25.D2; \
	VEOR	V25.B16, V2.B16, V2.B16; \
	\
	VLD1	(IN0), [V25.D1]; ADD $8, IN0; \
	VLD1	(IN1), [V26.D1]; ADD $8, IN1; \
	VZIP1	V26.D2, V25.D2, V25.D2; \
